categorize_flag() {
  if [[ "$1" == -Wanalyzer* || "$1" == "-fanalyzer" ]]; then echo "Analyzer";
  elif [[ "$1" == -W* ]]; then echo "Warning";
  elif [[ "$1" == -O* ]]; then echo "Optimization";
  elif [[ "$1" == -g* ]]; then echo "Debugging";
  elif [[ "$1" == -fsanitize* || "$1" == -fno-sanitize* ]]; then echo "Sanitizer";
  elif [[ "$1" == -f* ]]; then echo "Codegen";
  else echo "Other"; fi
}

for COMPILER in "${COMPILERS[@]}"; do
  echo "Testing compiler: $COMPILER"
  mkdir -p "flag_results/$COMPILER"
  rm -f flag_results/temp_${COMPILER}_*.txt

  for FLAG in "${FLAGS[@]}"; do
    SAFE_FLAG=$(echo "$FLAG" | sed 's#[/=,<>|]#_#g')
    echo "Trying $FLAG"

    SRC="test.c"
    EXTRA_FLAG=""
    if [[ "$FLAG" == *"c++"* ]]; then
      SRC="test.cpp"
      EXTRA_FLAG="-std=$CPP_STANDARD"
    elif [[ "$FLAG" == *"c11"* || "$FLAG" == *"c17"* ]]; then
      SRC="test.c"
      EXTRA_FLAG="-std=$C_STANDARD"
    fi

    set +e
    $COMPILER -Werror "$FLAG" $EXTRA_FLAG "$SRC" -o /tmp/test_output 2> "flag_results/$COMPILER/${SAFE_FLAG}.log"
    RESULT=$?
    set -e
    CATEGORY=$(categorize_flag "$FLAG")

    if [ $RESULT -eq 0 ]; then
      echo "$FLAG" >> "flag_results/temp_${COMPILER}_${CATEGORY}_supported.txt"
    else
      echo "$FLAG" >> "flag_results/temp_${COMPILER}_${CATEGORY}_unsupported.txt"
    fi

    rm -f /tmp/test_output
  done

  # Write supported flags grouped by category
  SUP_FILE="flag_results/${COMPILER}_supported_flags.txt"
  rm -f "$SUP_FILE"
  for FILE in flag_results/temp_${COMPILER}_*_supported.txt; do
    if [ -f "$FILE" ]; then
      CATEGORY=$(echo "$FILE" | sed 's#.*temp_$COMPILER_\(.*\)_supported.txt#\1#')
      echo "# $CATEGORY Flags" >> "$SUP_FILE"
      cat "$FILE" >> "$SUP_FILE"
      echo "" >> "$SUP_FILE"
    fi
  done

  # Write unsupported flags grouped by category
  UNSUP_FILE="flag_results/${COMPILER}_unsupported_flags.txt"
  rm -f "$UNSUP_FILE"
  for FILE in flag_results/temp_${COMPILER}_*_unsupported.txt; do
    if [ -f "$FILE" ]; then
      CATEGORY=$(echo "$FILE" | sed 's#.*temp_$COMPILER_\(.*\)_unsupported.txt#\1#')
      echo "# $CATEGORY Flags" >> "$UNSUP_FILE"
      cat "$FILE" >> "$UNSUP_FILE"
      echo "" >> "$UNSUP_FILE"
    fi
  done

  # Clean up temp files
  rm -f flag_results/temp_${COMPILER}_*.txt
done

echo "Finished."